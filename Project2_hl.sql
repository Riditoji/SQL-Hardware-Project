CREATE TABLE CUSTOMER_hl(
CustID		CHAR(5),
CustFName	VARCHAR(15)	NOT NULL,
CustLName	VARCHAR(15)	NOT NULL,
CustPhone	CHAR(10),
PRIMARY KEY (CustID)
);
CREATE TABLE PRODUCTCAT_hl(
ProdCatID	CHAR(1),
ProdCatName	VARCHAR(15)	NOT NULL,
PRIMARY KEY (ProdCatID)
);
CREATE TABLE COMMISSION_hl(
CommClass	CHAR(1),
CommRate	VARCHAR(4),
PRIMARY KEY (CommClass)
);
CREATE TABLE DEPT_hl(
DeptID		CHAR(2),
DeptName	VARCHAR(12),
PRIMARY KEY (DeptID)
);
CREATE TABLE PRODUCT_hl(
ProductID	CHAR(3),
ProdName	VARCHAR(20)	NOT NULL,
ProdCatID	CHAR(3),
PRIMARY KEY (ProductID),
FOREIGN KEY (ProdCatID) REFERENCES PRODUCTCAT_hl(ProdCatID)
);

CREATE TABLE SALESREP_hl(
SalesRepID	VARCHAR(5),
SalesRepFName	VARCHAR(15)	NOT NULL,
SalesRepLName	VARCHAR(15)	NOT NULL,
DeptID		CHAR(2),
CommClass	CHAR(1)	,
PRIMARY KEY (SalesRepID),
FOREIGN KEY (DeptID) REFERENCES DEPT_hl(DeptID)
);
CREATE TABLE ORDER_hl(
OrderID		CHAR(3),
OrderDate	VARCHAR(10),
CustID		VARCHAR(5),
SalesRepID	VARCHAR(5),
PRIMARY KEY (OrderID),
FOREIGN KEY (CustID) REFERENCES CUSTOMER_hl(CustID),
FOREIGN KEY (SalesRepID) REFERENCES SALESREP_hl(SalesRepID)
);
CREATE TABLE ORDERDETAIL_hl(
OrderID		CHAR(3),
ProductID	CHAR(3),
ProdQty		CHAR(3),
ProdPrice	VARCHAR(6),
FOREIGN KEY (OrderID) REFERENCES ORDER_hl(OrderID),
FOREIGN KEY (ProductID) REFERENCES PRODUCT_hl(ProductID)
);

DESCRIBE CUSTOMER_hl;
DESCRIBE PRODUCTCAT_hl;
DESCRIBE COMMISSION_hl;
DESCRIBE DEPT_hl;
DESCRIBE PRODUCT_hl;
DESCRIBE SALESREP_hl;
DESCRIBE ORDER_hl;
DESCRIBE ORDERDETAIL_hl;

